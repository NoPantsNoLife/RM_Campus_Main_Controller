uint8_t SPI_ReadWriteByte(SPI_TypeDef *SPIx, uint8_t TxData)
{
	uint8_t resv_buff = 0;
	LL_SPI_StartMasterTransfer(SPIx);
	while (LL_SPI_IsActiveFlag_TXP(SPIx) == 0);
	LL_SPI_TransmitData8(SPIx, TxData);
	while (LL_SPI_IsActiveFlag_RXP(SPIx) == 0);
	resv_buff = LL_SPI_ReceiveData8(SPIx);
	//LL_SPI_ClearFlag_TXTF(SPIx);
	//LL_SPI_ClearFlag_EOT(SPIx);
	//LL_SPI_SuspendMasterTransfer(SPIx);
	return resv_buff;
}

